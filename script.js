// --- 功能 ---

// 将 Unicode 码点转换为字符
function getIconChar(code) {
    return String.fromCharCode(parseInt(code, 16));
}

// 将图标代码转换为不同格式
function formatIconCode(code, format) {
    switch(format) {
        case 'unicode':
            return `U+${code}`;
        case 'html':
            return `&#x${code};`;
        case 'url':
            // 转换为URL编码格式
            const char = getIconChar(code);
            let urlEncoded = '';
            for (let i = 0; i < char.length; i++) {
                urlEncoded += '%' + char.charCodeAt(i).toString(16).toUpperCase().padStart(2, '0');
            }
            return urlEncoded;
        default:
            return `U+${code}`;
    }
}

// 获取当前选择的复制格式
function getSelectedFormat() {
    const formatRadios = document.querySelectorAll('input[name="copyFormat"]');
    for (const radio of formatRadios) {
        if (radio.checked) {
            return radio.value;
        }
    }
    return 'unicode'; // 默认为Unicode格式
}

// 显示复制成功提示
let notificationTimeout;
function showCopyNotification() {
    clearTimeout(notificationTimeout); // 清除之前的计时器
    copyNotification.classList.add('show');
    notificationTimeout = setTimeout(() => {
        copyNotification.classList.remove('show');
    }, 1500); // 1.5秒后隐藏
}

// 复制文本到剪贴板
function copyToClipboard(text) {
    navigator.clipboard.writeText(text)
        .then(() => {
            console.log('Copied:', text);
            showCopyNotification();
        })
        .catch(err => {
            console.error('Failed to copy text: ', err);
            // 可以添加一个错误提示
        });
}

// 创建单个图标元素
function createIconItem(icon) {
    const item = document.createElement('div');
    item.classList.add('icon-item');
    item.dataset.code = icon.code; // 存储代码，方便复制

    // 图标本身
    const glyph = document.createElement('div');
    glyph.classList.add('icon-glyph');
    glyph.textContent = getIconChar(icon.code);
    glyph.title = `${icon.name_en} / ${icon.name_zh || icon.name_en}`; // Tooltip

    // 英文名
    const nameEn = document.createElement('div');
    nameEn.classList.add('icon-name-en');
    nameEn.textContent = icon.name_en;

    // 中文名（如果存在）
    const nameZh = document.createElement('div');
    nameZh.classList.add('icon-name-zh');
    nameZh.textContent = icon.name_zh || '-'; // 如果没有中文名，显示 '-'

    // Unicode 代码
    const code = document.createElement('div');
    code.classList.add('icon-code');
    code.textContent = `\\u${icon.code}`; // 显示为 \uXXXX 格式

    item.appendChild(glyph);
    item.appendChild(nameZh);
    item.appendChild(nameEn);
    item.appendChild(code);

    // 添加点击复制功能
    item.addEventListener('click', () => {
        const format = getSelectedFormat();
        let textToCopy;
        
        if (format === 'unicode' && !document.querySelector('input[value="unicode"]').hasAttribute('data-use-code')) {
            // 复制图标字符本身（默认的Unicode字符行为）
            textToCopy = getIconChar(icon.code);
        } else {
            // 复制不同格式的编码
            textToCopy = formatIconCode(icon.code, format);
        }
        
        copyToClipboard(textToCopy);
    });

    return item;
}

// 渲染所有图标
function renderIcons(iconsToRender) {
    iconGrid.innerHTML = ''; // 清空现有图标
    iconsToRender.forEach(icon => {
        const iconItem = createIconItem(icon);
        iconGrid.appendChild(iconItem);
    });
}

// 过滤图标
function filterIcons() {
    const searchTerm = searchInput.value.toLowerCase().trim();
    const allItems = iconGrid.querySelectorAll('.icon-item');

    allItems.forEach(item => {
        const nameEn = item.querySelector('.icon-name-en').textContent.toLowerCase();
        const nameZh = item.querySelector('.icon-name-zh').textContent.toLowerCase();
        const code = item.dataset.code.toLowerCase();

        // 检查英文名、中文名或代码是否匹配
        const isMatch = nameEn.includes(searchTerm) ||
                       (nameZh !== '-' && nameZh.includes(searchTerm)) ||
                       code.includes(searchTerm) ||
                       `\\u${code}`.includes(searchTerm); // 也允许搜索 \uXXXX

        if (isMatch) {
            item.classList.remove('hidden');
        } else {
            item.classList.add('hidden');
        }
    });
}

// 更新格式说明
function updateFormatExample() {
    const format = getSelectedFormat();
    const exampleCode = 'E72B'; // 后退图标作为示例
    
    const labels = document.querySelectorAll('.format-options label');
    labels.forEach(label => {
        const radio = label.querySelector('input[type="radio"]');
        const formatValue = radio.value;
        
        // 重置所有标签文本
        label.innerHTML = `<input type="radio" name="copyFormat" value="${formatValue}"${formatValue === format ? ' checked' : ''}> ${getFormatName(formatValue)}`;
        
        // 为当前选中的格式添加示例
        if (formatValue === format) {
            const example = document.createElement('span');
            example.classList.add('format-example');
            example.textContent = ` (${formatIconCode(exampleCode, formatValue)})`;
            label.appendChild(example);
        }
    });
}

// 获取格式的友好名称
function getFormatName(format) {
    switch(format) {
        case 'unicode': return 'Unicode字符';
        case 'html': return 'HTML/XAML编码';
        case 'url': return 'URL编码';
        default: return '未知格式';
    }
}

